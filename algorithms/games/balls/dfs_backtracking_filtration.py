def solve(current_state: [[int]], prev_states: [[[int]]], curr_moves: [(int, int)], prev_moves: [(int, int)], n):
    print(n)
    # Win condition
    if check_win(current_state):
        print('Я сделаль')
        print_state(current_state)
        return curr_moves

    possible_moves = [
        move for move in get_possible_moves(current_state) if not is_cycle(current_state, prev_states, move, prev_moves)
    ]

    # Loss condition
    if len(possible_moves) == 0 and len(curr_moves) == 0:
        print('Я не смог... надеюсь, ты будешь лучшим Глубиная Первый Поиск чем я.')
        return None

    # Backtrack condition
    if len(possible_moves) == 0 and len(curr_moves) > 0:
        r_b, r_e = reversed(curr_moves.pop())
        make_step(current_state, (r_b, r_e), curr_moves, prev_moves, back=True)
        return solve(current_state, prev_states, curr_moves, prev_moves, n + 1)

    # Next move
    for move in possible_moves:
        prev_states.append(copy_state(current_state))
        make_step(current_state, move, curr_moves, prev_moves, back=False)
        return solve(current_state, prev_states, curr_moves, prev_moves, n + 1)


def get_possible_moves(cur_state: [[int]]):
    possible_places = [flask for flask in cur_state if not is_full(flask['balls'])]
    possible_travelers = [flask for flask in cur_state if not is_empty(flask['balls'])]

    possible_moves = []

    for traveler in possible_travelers:
        for place in possible_places:
            if traveler['index'] != place['index'] and \
                    (is_empty(place['balls']) or traveler['balls'][-1] == place['balls'][-1]):
                possible_moves.append((traveler['index'], place['index']))

    return possible_moves


def make_step(current_state: [[int]], move: (int, int), curr_moves: [(int, int)], prev_moves: [(int, int)], back=False):
    i_begin, i_end = move

    if i_begin != i_end and \
            not is_empty(current_state[i_begin]['balls']) and \
            (is_empty(current_state[i_end]['balls']) or
             (not is_full(current_state[i_end]['balls']) and
              current_state[i_begin]['balls'][-1] == current_state[i_begin]['balls'][-1])):

        current_state[i_end]['balls'].append(current_state[i_begin]['balls'].pop())
        if not back:
            curr_moves.append((i_begin, i_end))
            prev_moves.append((i_begin, i_end))

    elif back:
        current_state[i_end]['balls'].append(current_state[i_begin]['balls'].pop())
        print('Понял, выполняю!?')

    else:
        print('Ты кто такой чтоб это делать!?')


def is_cycle(curr_state: [[int]], prev_states: [[[int]]], curr_move: (int, int), prev_moves: [(int, int)]):
    same_moves_indexes = [index for index, move in enumerate(prev_moves) if move == curr_move]

    for index in same_moves_indexes:
        if states_equals(curr_state, prev_states[index]):
            return True

    return False


def check_win(current_state: [int]) -> bool:
    for flask in current_state:
        if not (is_uniform(flask['balls']) and is_full(flask['balls']) or is_empty(flask['balls'])):
            return False

    return True


def is_empty(array):
    return len(array) == 0


def is_full(array):
    return len(array) == 4


def is_uniform(array):
    if is_empty(array):
        return True

    first = array[-1]

    for ball in array:
        if ball != first:
            return False

    return True


def copy_state(state):
    return [{'index': flask['index'], 'balls': flask['balls'][:]} for flask in state]


def states_equals(first_state: [[]], second_state: [[]]):
    def flask_equals(first_flask: [int], second_flask: [int]) -> bool:
        if len(first_flask['balls']) != len(second_flask['balls']):
            return False

        for f_i in range(len(first_flask['balls'])):
            if first_flask['balls'][f_i] != second_flask['balls'][f_i]:
                return False

        return True

    for i in range(len(first_state)):
        if not flask_equals(first_state[i], second_state[i]):
            return False

    return True


